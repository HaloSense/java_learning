package com.halosense.arithmeticOperator;

public class ArithmeticOperatorDemo {
    static void main(String[] args) {
        // +
        System.out.println(3 + 2);
        // -
        System.out.println(5 - 1);
        // *
        System.out.println(7 * 9);

        // 如果有小数参与计算，结果有可能不精确
        System.out.println(1.1 + 1.01);



    }
}
